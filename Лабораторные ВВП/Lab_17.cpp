void n4()
{
    int N, i, arr[100], max = -1000;
    cout << "Введите размер N массива A, чтобы найти номер последнего локального максимума" << endl;
    cout << "Введите размер массива: ";
    cin >> N;
    cout << "Введите элементы массива через Enter: " << endl;
    for (i = 0; i < N; i++)
    {
        cin >> arr[i];
    }
    if (arr[0] > arr[1])
    {
        max = 1;
    }
    for (i = 1; i < N - 1; i++)
    {
        if (arr[i - 1] < arr[i] and arr[i] > arr[i + 1])
        {
            max = i + 1;
        }
    }
    if (arr[N - 1] > arr[N - 2])
    {
        max = N;
    }
    cout << "Номер последнего локального максимума: " << max << endl;
}

