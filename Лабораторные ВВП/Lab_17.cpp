void n2()
{
    int N, arr[100], i, x, k = 0;
    cout << "Введите размер массива N, не содержащий одинаковых чисел, чтобы вывести разность прогрессии,\nесли числа её образуют, и 0, если нет." << endl;
    cout << "Введите размер массива N: ";
    cin >> N;
    cout << "Введите сам массив через Enter: " << endl;
    for (i = 0; i < N; i++)
    {
        cin >> arr[i];
    }
    x = arr[1] - arr[0];
    for (i = 1; i < N; i++)
    {
        if (arr[i] - arr[i - 1] == x)
        {
            k++;
        }
    }
    if (k == N - 1)
    {
        cout << "Числа массива образуют арифметическую прогрессию с разностью в " << x << endl;
    }
    else
    {
        cout << 0 << endl;
    }
}

